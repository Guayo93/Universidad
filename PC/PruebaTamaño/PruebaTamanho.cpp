#include <iostream>

using namespace std;

int main()
{
    int x;
    cout << "El tamaño de int es " << sizeof(x) << endl;

    short y;
    cout << "El tamaño de short es " << sizeof(y) << endl;

    long z;
    cout << "El tamaño de long es " << sizeof(z) << endl;

    char a;
    cout << "El tamaño de char es " << sizeof(a) << endl;

    float b;
    cout << "El tamaño de float es " << sizeof(b) << endl;

    double c;
    cout << "El tamaño de double es " << sizeof(c) << endl;

    long double d;
    cout << "El tamaño de long double es " << sizeof(d) << endl;

    long long e;
    cout << "El tamaño de long long es " << sizeof(e) << endl;

    bool f;
    cout << "El tamaño de bool es " << sizeof(f) << endl;

    short int g;
    cout << "El tamaño de short int es " << sizeof(g) << endl;
}
